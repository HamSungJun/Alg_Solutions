package Baekjoon.Lev_14;

public class PositionNode {

    private int xPos;
    private int yPos;

    public PositionNode(int xPos, int yPos) {

        this.xPos = xPos;
        this.yPos = yPos;

    }

    public int getX() {
        return this.xPos;
    }

    public int getY() {
        return this.yPos;
    }

}